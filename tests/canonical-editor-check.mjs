import fs from "node:fs";
import assert from "node:assert/strict";
import { createCanonicalEditorSession, readEditorValue, replaceEditorValue, writeEditorDraft } from "../src/core/canonical-editor.js";
import { createWorkspace, canonicalStringify, verifyWorkspace } from "../src/core/workspace-contract.js";

const analysis = JSON.parse(fs.readFileSync(new URL("../fixtures/sample-analysis-en.json", import.meta.url), "utf8"));
const ids = ["ws_editor", "rev_editor", "draft_editor", "evt_editor"];
const workspace = await createWorkspace({ analysis, manifest: { id: "strategic", contractId: "strategic-analysis-v1", schemaVersion: "1.1.0" }, clock: () => "2026-07-22T10:00:00.000Z", idFactory: () => ids.shift() });
const importedBefore = canonicalStringify(workspace.revisions[0].canonical_payload);
const validate = (payload) => ({ valid: Boolean(payload.subject?.title), errors: payload.subject?.title ? [] : [{ path: "/subject/title" }] });
const editor = createCanonicalEditorSession({ payload: workspace.working_draft.canonical_payload, validate, historyLimit: 3 });
assert.equal(readEditorValue(editor.inspect().payload, "/subject/title"), analysis.subject.title);
editor.replace("/subject/title", "Edited title");
assert.equal(editor.inspect().dirty, true);
assert.equal(editor.inspect().canUndo, true);
editor.undo();
assert.equal(readEditorValue(editor.inspect().payload, "/subject/title"), analysis.subject.title);
assert.equal(editor.inspect().canRedo, true);
editor.redo();
assert.equal(readEditorValue(editor.inspect().payload, "/subject/title"), "Edited title");
assert.equal(editor.inspect().canRedo, false);
editor.replace("/subject/title", "");
assert.equal(editor.inspect().validation.valid, false);
editor.undo();
const next = await writeEditorDraft(workspace, editor.inspect().payload, { clock: () => "2026-07-22T10:05:00.000Z" });
assert.equal(next.repository_revision, 2);
assert.equal(next.working_draft.dirty, true);
assert.notEqual(next.working_draft.payload_checksum, workspace.working_draft.payload_checksum);
assert.equal(canonicalStringify(next.revisions[0].canonical_payload), importedBefore, "editor mutated immutable imported revision");
await verifyWorkspace(next);
assert.throws(() => replaceEditorValue(analysis, "/missing/value", 1), /does not exist/);
console.log("Structured canonical editor contract checks passed.");
